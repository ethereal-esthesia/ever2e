		int mods = 0;
		if( (sdlMods&SDLKeycode.SDL_KMOD_SHIFT)!=0 )
			mods |= Event.SHIFT_MASK;
		if( (sdlMods&SDLKeycode.SDL_KMOD_CTRL)!=0 )
			mods |= Event.CTRL_MASK;
		if( (sdlMods&SDLKeycode.SDL_KMOD_ALT)!=0 )
			mods |= Event.ALT_MASK;
